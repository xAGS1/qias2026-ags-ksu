from __future__ import annotations

from textwrap import dedent
from .heirs import HEIRS
import json


def build_heirs_extraction_prompt(heirs_segments: list[str]) -> str:
    segments_json = json.dumps(heirs_segments, ensure_ascii=False)
    heirs_list = "، ".join(sorted(list(HEIRS)))

    return dedent(f"""
        كل مقطع يمثل نوعًا واحدًا من الورثة، وعدد الورثة قد يكون مذكورًا صراحة أو مفهومًا ضمنًا.

        قائمة المقاطع (ممنوع الإضافة أو الحذف أو الدمج):
        {segments_json}

        المطلوب لكل مقطع:
        1) استخرج العدد:
           - إذا وُجد عدد صريح مثل: واحد، اثنان، ثلاثة، أربعة، خمسة... أو رقم مثل 2 أو 3 أو 4 → count = العدد
           - إذا لم يُذكر عدد صراحة → count = 1

        2) حوّل اسم الوارث إلى صيغة المفرد فقط مع الحفاظ على كامل سلسلة النسب:
           - أبناء ⟶ ابن  (فقط الكلمة الأولى تتغير، والباقي يُحفظ كما هو)
           - بنات ⟶ بنت
           - إخوة ⟶ أخ
           - أخوان ⟶ أخ
           - أخوات ⟶ أخت
           - أعمام ⟶ عم
           - زوجات ⟶ زوجة

           ⚠️ تحذير مهم: سلسلة النسب الكاملة يجب أن تُنقل بالكامل، لا تحذف أي جزء منها:
           - "خمسة أبناء ابن ابن" ⟶ {{ "heir": "ابن ابن ابن", "count": 5 }}
             (أبناء → ابن، ثم يبقى "ابن ابن" كما هو ⟹ النتيجة: "ابن ابن ابن")
           - "أربعة أبناء أخ لأب" ⟶ {{ "heir": "ابن أخ لأب", "count": 4 }}
           - "أخوان لأب" ⟶ {{ "heir": "أخ لأب", "count": 2 }}
           - "ثلاثة أبناء ابن" ⟶ {{ "heir": "ابن ابن", "count": 3 }}
             (لا تحذف "ابن" الثانية)

        3) يجب أن يكون اسم الوارث النهائي موجودًا حرفيًا في قائمة HEIRS الرسمية فقط:
           {heirs_list}

        4) ممنوع:
           - اختراع وارث جديد
           - تغيير الاسم إلى اسم غير موجود في HEIRS
           - دمج مقطعين في عنصر واحد
           - حذف أي مقطع
           - حذف أي جزء من سلسلة النسب (مثل تحويل "ابن ابن ابن" إلى "ابن")

        أرجع JSON فقط، وبنفس عدد المقاطع في الإدخال، وبالصيغة التالية فقط:
        {{
          "heirs": [
            {{"heir": "...", "count": 0}}
          ]
        }}
    """)


def build_reasoning_prompt(question: str, extracted_heirs: list) -> str:
    formatted = "\n".join(
        f"- {h.get('heir')} (عددهم: {h.get('count', 1)})"
        for h in extracted_heirs
        if isinstance(h, dict) and isinstance(h.get("heir"), str)
    )

    return dedent(f"""
        أنت فقيه متخصص في علم الفرائض على مذهب الجمهور، وتُحلّ المسألة حلًا شرعيًا دقيقًا ومنضبطًا حسابيًا.

        هذه القائمة هي الورثة المستخرجون من نص المسألة، وممنوع إضافة أي وارث أو حذف أي وارث أو تعديل اسمه أو عدده:
        {formatted}

        نص المسألة:
        "{question}"

        المطلوب:
        حلّ المسألة اعتمادًا على هذه القائمة فقط، مع الالتزام بالترتيب الآتي داخل الشرح:
        - بيان من يرث ومن يُحجب، مع سبب الحجب إن وُجد
        - بيان نوع استحقاق كل وارث: فرض أو تعصيب
        - إعطاء أصحاب الفروض فروضهم أولًا
        - ثم توزيع الباقي على العصبات المستحقين فقط إن وجد باقي
        - التحقق الصريح من أن مجموع الأنصباء النهائية يساوي كامل التركة
        - إذا زادت الفروض على أصل المسألة فاذكر أن فيها عول
        - إذا بقي شيء ولا يوجد عاصب مستحق فاذكر هل فيها رد
        - إن أمكن، أدرج دليلًا شرعيًا مناسبًا من القرآن أو السنة

        قواعد إلزامية:
        1) لا تضف وارثًا غير موجود في القائمة.
        2) لا تغيّر عدد أي وارث.
        3) لا يجوز أن يظهر وارث في المحجوبين ثم يظهر مرة أخرى في الأنصباء.
        4) لا يجوز أن يأخذ وارثان مختلفان "باقي التركة" كلٌّ على حدة.
        5) لا تذكر نسبًا مئوية أو كسورًا نهائية إلا بعد التحقق من صحة المجموع.
        6) إذا تعذر ضبط القسمة يقينًا، فصرّح بوجود إشكال حسابي بدل اختراع قسمة غير منضبطة.
        7) قواعد الجدات:
           - الجدة الصحيحة ترث السدس بشرط عدم وجود الأم أو جدة أقرب منها.
           - طبّق الحكم الشرعي الصحيح بناءً على من هو موجود في قائمة الورثة فقط.

        اكتب الحل في فقرة واحدة أو عدة فقرات متصلة بلغة فقهية واضحة،
        ومن غير عناوين، ومن غير ترقيم مثل: أولًا، ثانيًا، ثالثًا.
    """)


def build_structured_extraction_prompt(
    question: str,
    reasoning: str,
    extracted_heirs: list,
) -> str:
    return dedent(f"""
        استخرج JSON فقط من الشرح التالي، بدون أي نص إضافي، وبدون إعادة حل المسألة من جديد.

        المسألة:
        "{question}"

        الشرح:
        \"\"\"{reasoning}\"\"\"

        قائمة الورثة الأصلية المسموح بها فقط (مع العدد، وممنوع التعديل):
        {json.dumps(extracted_heirs, ensure_ascii=False)}

        القواعد الصارمة:

        1) heirs:
           - ⚠️ انسخ جميع الورثة من القائمة الأصلية كما هي تمامًا، بدون إضافة أو حذف أو تعديل.
           - يجب أن يكون عدد العناصر في heirs مساويًا تمامًا لعدد عناصر القائمة الأصلية.
           - الورثة المحجوبون يُدرجون في heirs أيضًا (heirs = الكل، blocked = المحجوبون منهم فقط).

        2) blocked:
           - اذكر فقط الورثة المحجوبين من القائمة الأصلية.
           - يجب أن يكونوا من نفس القائمة الأصلية.
           - وجودهم في blocked لا يعفي من إدراجهم في heirs.

        3) shares:
           - اذكر فقط الورثة الذين لهم نصيب فعلي (غير المحجوبين).
           - الفرض ⟶ fraction = "a/b"
           - التعصيب ⟶ fraction = "باقي التركة" أو "كل التركة"
           - count يجب أن يطابق العدد الأصلي تمامًا.

        4) awl_or_radd:
           - قيمة واحدة فقط من: "عول" أو "رد" أو "لا"

        5) awl_stage (مطلوب فقط إذا كان awl_or_radd = "عول" أو "رد"):
           - asl_after_awl: أصل المسألة بعد العول أو الرد (عدد صحيح)
           - distribution: قائمة بنصيب كل وارث بعد العول/الرد (per_head_shares فقط، بدون نسبة مئوية)
           - إذا كان awl_or_radd = "لا" → awl_stage = null

        6) post_tasil:
           - total_shares: أصل المسألة بعد التصحيح (عدد صحيح يُقسَّم عليه)
             مثال: إذا كان الورثة 5 أبناء يقتسمون كل التركة بالتساوي → total_shares = 5
             مثال: إذا كانت أم (1/6) و 3 أبناء (5/6 بالتساوي) وأصل المسألة 6 → total_shares = 6
           - ممنوع إدراج وارث بنصيب 0%
           - per_head_percent: نسبة نصيب الفرد الواحد فقط (وليس نصيب المجموعة كلها)
             مثال: 5 أبناء يأخذون كل التركة → per_head_percent لكل واحد = 100 ÷ 5 = 20.0
             مثال: 5 أبناء يأخذون 5/6 → per_head_percent لكل واحد = (5/6 ÷ 5) × 100 = 16.67
           - per_head_percent رقم فقط مثل: 20.0

        7) ممنوع:
           - اختراع وارث جديد
           - تعديل اسم وارث
           - تعديل العدد
           - حذف أي وارث من heirs (حتى المحجوب)
           - وضع وارث في blocked ثم تكراره في shares
           - استنتاج معلومات غير موجودة في الشرح

        إذا كان الشرح نفسه متناقضًا أو غير كافٍ، فاستخرج ما ورد فقط، ولا تخترع تصحيحًا من عندك.

        أرجع JSON فقط بالصيغة التالية:

        {{
          "heirs": [
            {{"heir": "...", "count": 0}}
          ],
          "blocked": [
            {{"heir": "...", "count": 0}}
          ],
          "shares": [
            {{"heir": "...", "count": 0, "fraction": "..."}}
          ],
          "awl_or_radd": "عول|رد|لا",
          "awl_stage": {{
            "asl_after_awl": 0,
            "distribution": [
              {{
                "heir": "...",
                "count": 0,
                "per_head_shares": "a/b"
              }}
            ]
          }},
          "post_tasil": {{
            "total_shares": 0,
            "distribution": [
              {{
                "heir": "...",
                "count": 0,
                "per_head_shares": "a/b",
                "per_head_percent": 0
              }}
            ]
          }}
        }}
    """)


def build_retry_structured_prompt(
    question: str,
    reasoning: str,
    extracted_heirs: list,
    validation_errors: list[str],
) -> str:
    return dedent(f"""
        الشرح السابق نتج عنه JSON غير صالح أو متناقض.

        المسألة:
        "{question}"

        الشرح:
        \"\"\"{reasoning}\"\"\"

        قائمة الورثة الأصلية:
        {json.dumps(extracted_heirs, ensure_ascii=False)}

        أخطاء التحقق التي يجب تجنبها:
        {json.dumps(validation_errors, ensure_ascii=False)}

        أعد استخراج JSON فقط بصيغة منضبطة، مع الالتزام بما يلي:
        - heirs يجب أن يحتوي على جميع الورثة من القائمة الأصلية (بما فيهم المحجوبون)
        - لا تضف وارثًا جديدًا
        - لا تعدل الأعداد
        - لا تضع وارثًا في blocked و shares معًا
        - لا تجعل مجموع أصحاب الفروض يتجاوز التركة مع awl_or_radd = "لا"
        - لا تعطِ أكثر من وارث مستقل "باقي التركة" أو "كل التركة"
        - إذا كان الشرح غير كافٍ، استخرج الحد الأدنى الصحيح فقط دون اختراع

        أرجع JSON فقط بالصيغة نفسها المطلوبة سابقًا.
    """)